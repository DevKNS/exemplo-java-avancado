    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        linhaSelecionada = jTableMostrar.getSelectedRow();
        editando = true;
        String marca = jTableMostrar.getValueAt(linhaSelecionada, 0).toString();
        String modelo = jTableMostrar.getValueAt(linhaSelecionada, 1).toString();
        int ano = Integer.parseInt(jTableMostrar.getValueAt(linhaSelecionada, 2).toString());
        String batido = jTableMostrar.getValueAt(linhaSelecionada, 3).toString();
        String cor = jTableMostrar.getValueAt(linhaSelecionada, 4).toString();
        Double quilometragem = Double.parseDouble(jTableMostrar.getValueAt(linhaSelecionada, 5).toString());

        jComboBoxMarca.setSelectedItem(marca);
        jTextFieldModelo.setText(modelo);
        jComboBoxAno.setSelectedItem(ano);
        //jCheckBoxBatido.setSelected(batido);
        if (cor.equals("Vermelho")){
            jRadioButtonVermelho.setSelected(true);
//            
        }else if(cor.equals("Preto")){
            jRadioButtonPreto.setSelected(true);   
        }else if(cor.equals("Branco")){
           jRadioButtonBranco.setSelected(true);
        }
        limparCampos();
        


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        String modeloCarro = jTextFieldModelo.getText().trim();
        String marca = jComboBoxMarca.getSelectedItem().toString();
        int ano = Integer.parseInt(jComboBoxAno.getSelectedItem().toString());
        String cor = "";
        if (jRadioButtonPreto.isSelected() == true) {
            cor = "Preto";

        } else if (jRadioButtonBranco.isSelected() == true) {
            cor = "Branco";
        } else if (jRadioButtonVermelho.isSelected() == true) {
            cor = "Vermelho";
        }
        String batido = "";
        if (jCheckBoxBatido.isSelected() == true) {
            batido = "sim";
        } else if (jCheckBoxBatido.isSelected() == false) {
            batido = "n√£o";
        }
        double quilometragem = Double.parseDouble(jFormattedTextFieldQuilometragem.getText().toString());
        DefaultTableModel modeloTabela = (DefaultTableModel) jTableMostrar.getModel();
        if (editando == false) {
            modeloTabela.addRow(new Object[]{
                marca, modeloCarro, ano, batido, cor, quilometragem});
        } else {
            jTableMostrar.setValueAt(marca, linhaSelecionada, 0);
            jTableMostrar.setValueAt(modeloCarro, linhaSelecionada, 1);
            jTableMostrar.setValueAt(ano, linhaSelecionada, 2);
            jTableMostrar.setValueAt(batido, linhaSelecionada, 3);
            jTableMostrar.setValueAt(cor, linhaSelecionada, 4);
            jTableMostrar.setValueAt(quilometragem, linhaSelecionada, 5);
            linhaSelecionada = -1;
            editando = false;

        }
        limparCampos();

    }//GEN-LAST:event_jButton4ActionPerformed

    private void limparCampos(){
        jTextFieldModelo.setText("");
        jComboBoxMarca.setSelectedItem(-1);
        jRadioButtonPreto.setSelected(false);
        jRadioButtonBranco.setSelected(false);
        jRadioButtonVermelho.setSelected(false);
        jCheckBoxBatido.setSelected(false);
        jComboBoxAno.setSelectedItem(-1);
        jFormattedTextFieldQuilometragem.setText("");
        return;
        
        
    }
    
    
