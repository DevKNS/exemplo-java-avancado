    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        String precoTexto = jFormattedTextFieldPreco.getValue().toString();
        precoTexto = precoTexto
                .replace("R$", "")
                .replace(" ","")
                .replace(",",".");
        int quantidade  = Integer.parseInt(jSpinnerDia.getValue().toString());
        double preco = Double.parseDouble(precoTexto);
        double total = quantidade * preco;
        DecimalFormat formato = new DecimalFormat("R$ ##.00");
        JOptionPane.showMessageDialog(this,
                "Quantidade"+ quantidade +
                        "\nPreco: " + formato.format(preco).replace(".",",") +
                        "\nTotal: " + formato.format(total).replace(".",","));
    }//GEN-LAST:event_jButtonSalvarActionPerformed

