    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed

        String nome = jTextFieldNome.getText().trim();
        String sobrenome = jTextFieldSobrenome.getText().trim();

        if (nome.length() < 6) {
            JOptionPane.showMessageDialog(this, "Nome deve conter pelo menos 6 caracteres");
            return;

        }
        if (sobrenome.length() < 6) {
            JOptionPane.showMessageDialog(this, "Digite um sobre nome com no minimo 6 caracteres");
        }
        //ObtÃ©m o modelo da tabela
        DefaultTableModel modelo = (DefaultTableModel) jTableTabela.getModel();

        //Adcionar uma nova linha na tabela
        modelo.addRow(new Object[]{
            nome, sobrenome
        });
        jTextFieldNome.setText("");
        jTextFieldSobrenome.setText("");
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

