public class ExemploString3 {

    public static void main(String[] args) {
        //Letra minuscula
        String nome = "Bom de Guerra";
        nome = nome.toLowerCase();
        System.out.println(nome);
        //Letra maiuscula
        nome = nome.toUpperCase();
        System.out.println(nome);
    }
}